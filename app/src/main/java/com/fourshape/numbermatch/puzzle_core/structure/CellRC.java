package com.fourshape.numbermatch.puzzle_core.structure;

public class CellRC {

    public int row, col;

    public CellRC (int row, int col) {
        this.row = row;
        this.col = col;
    }

    public int getRow() {
        return row;
    }

    public int getCol() {
        return col;
    }

}
